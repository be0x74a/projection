package v1_test

import (
	"bytes"
	"flag"
	"os"
	"path/filepath"
	"testing"
)

// updateGolden regenerates the committed CRD golden file from the currently
// rendered CRD. Invoke via `go test ./api/v1/... -update` or
// `make update-crd-golden`.
var updateGolden = flag.Bool("update", false,
	"regenerate the CRD golden file from the current rendered CRD")

// TestCRDGolden guards the v1 API stability promise. The rendered CRD
// (regenerated by `make manifests` before every `make test`) is compared
// byte-for-byte against a committed golden in testdata/. Any change to
// api/v1/*.go that affects the CRD schema fails this test until the golden
// is consciously regenerated alongside the code change, forcing both to
// land in the same PR and making the schema diff reviewable.
//
// See docs/api-stability.md for the permanence commitment this test enforces.
func TestCRDGolden(t *testing.T) {
	const (
		renderedPath = "../../config/crd/bases/projection.sh_projections.yaml"
		goldenPath   = "testdata/crd.golden.yaml"
	)

	rendered, err := os.ReadFile(renderedPath)
	if err != nil {
		t.Fatalf("reading rendered CRD at %s: %v\n\n"+
			"Did `make manifests` run? The rendered CRD must exist before this test runs.",
			renderedPath, err)
	}

	if *updateGolden {
		if err := os.MkdirAll(filepath.Dir(goldenPath), 0o755); err != nil {
			t.Fatalf("creating testdata dir: %v", err)
		}
		if err := os.WriteFile(goldenPath, rendered, 0o644); err != nil {
			t.Fatalf("writing golden at %s: %v", goldenPath, err)
		}
		t.Logf("updated golden at %s (%d bytes)", goldenPath, len(rendered))
		return
	}

	golden, err := os.ReadFile(goldenPath)
	if err != nil {
		t.Fatalf("reading golden at %s: %v\n\n"+
			"Run `make update-crd-golden` to initialise the golden file.",
			goldenPath, err)
	}

	if !bytes.Equal(rendered, golden) {
		t.Fatalf(`CRD schema drift detected.

The rendered CRD at %s differs from the committed golden at %s.

This test guards the v1 API stability promise (docs/api-stability.md).
A diff here means your change to api/v1/*.go altered the CRD. Read the
diff carefully — if the change is a breaking schema modification (rename,
remove, or semantic change to an existing field), it is forbidden by the
stability promise and must be reverted.

If the change is additive and safe (new optional field, new condition
reason), regenerate the golden:

    make update-crd-golden

and commit the updated golden alongside your change. Reviewers will see
both the types.go change and the golden diff in one PR.

-----BEGIN rendered-----
%s
-----END rendered-----

-----BEGIN golden-----
%s
-----END golden-----`,
			renderedPath, goldenPath, rendered, golden)
	}
}
